/*
 |--------------------------------------------------------------------------
 | Import mongoose's Schema.
 |--------------------------------------------------------------------------
 */
import moongoose, { Schema } from 'mongoose';

/*
 |--------------------------------------------------------------------------
 | Set up schema for user.
 |--------------------------------------------------------------------------
 */
const userSchema = new Schema({
    first_name: String,
    last_name: String,
    age: Number,
});

/*
 |--------------------------------------------------------------------------
 | Export user's table
 |--------------------------------------------------------------------------
 */
export default moongoose.model('user', userSchema);